package fake

import (
	"bytes"
	"io/ioutil"
	"net/http"
)

// FakeRetryingRoundTripper implements RetryingRoundTripperInterface
type FakeRetryingRoundTripper struct{}

// RoundTrip returns a response with status 200.
func (rrt FakeRetryingRoundTripper) RoundTrip(req *http.Request) (*http.Response, error) {
	body := "everything works fine."
	return &http.Response{
		Status:        "200 OK",
		StatusCode:    200,
		Proto:         "HTTP/1.1",
		ProtoMajor:    1,
		ProtoMinor:    1,
		Body:          ioutil.NopCloser(bytes.NewBufferString(body)),
		ContentLength: int64(len(body)),
		Request:       req,
		Header:        make(http.Header, 0),
	}, nil
}
